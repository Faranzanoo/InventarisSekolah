    private void tblPeminjamanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPeminjamanMouseClicked
    int baris = tblPeminjaman.getSelectedRow();
    idPeminjaman = tblPeminjaman.getValueAt(baris, 0).toString();
    txtNamaBarang.setText(tblPeminjaman.getValueAt(baris, 1).toString());
    txtNamaPeminjam.setText(tblPeminjaman.getValueAt(baris, 2).toString());

    try {
        String tgl = tblPeminjaman.getValueAt(baris, 3).toString();
        dcTglPinjam.setDate(new SimpleDateFormat("yyyy-MM-dd").parse(tgl));
    } catch (Exception e) {
        dcTglPinjam.setDate(null);
    }
    }//GEN-LAST:event_tblPeminjamanMouseClicked

