    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
            if (idPeminjaman == null) {
        JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus!");
        return;
    }
    int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (konfirmasi == JOptionPane.YES_OPTION) {
        try {
            Connection con = koneksi.Koneksi.getKoneksi();
            String sql = "DELETE FROM peminjaman WHERE id_peminjaman = ?";
            java.sql.PreparedStatement ps = con.prepareStatement(sql);
            ps.setInt(1, Integer.parseInt(idPeminjaman));
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
            tampilData();
            bersih();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
    String namaBarang = txtNamaBarang.getText();
    String namaPeminjam = txtNamaPeminjam.getText();

    if (namaBarang.isEmpty() || namaPeminjam.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Semua field harus diisi!");
        return;
    }
    if (dcTglPinjam.getDate() == null) {
        JOptionPane.showMessageDialog(this, "Tanggal harus diisi!");
        return;
    }

    String tgl = new SimpleDateFormat("yyyy-MM-dd").format(dcTglPinjam.getDate());

    try {
        Connection con = koneksi.Koneksi.getKoneksi();
        String sql = "INSERT INTO peminjaman (nama_barang, nama_peminjam, tanggal_pinjam, status_peminjaman) VALUES (?, ?, ?, 'Dipinjam')";
        java.sql.PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, namaBarang);
        ps.setString(2, namaPeminjam);
        ps.setString(3, tgl);
        ps.executeUpdate();
        JOptionPane.showMessageDialog(this, "Data peminjaman berhasil disimpan!");
        tampilData();
        bersih();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

