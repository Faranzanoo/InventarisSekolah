private void tampilData() {
    javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
    model.addColumn("ID");
    model.addColumn("Nama Barang");
    model.addColumn("Nama Peminjam");
    model.addColumn("Tgl Pinjam");
    model.addColumn("Status");

    try {
        Connection con = koneksi.Koneksi.getKoneksi();
        String sql = "SELECT * FROM peminjaman";
        java.sql.Statement st = con.createStatement();
        java.sql.ResultSet rs = st.executeQuery(sql);
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("id_peminjaman"),
                rs.getString("nama_barang"),
                rs.getString("nama_peminjam"),
                rs.getString("tanggal_pinjam"),
                rs.getString("status_peminjaman")
            });
        }
        tblPeminjaman.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}

